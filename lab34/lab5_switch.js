let day = "Monday";
switch (day) {
    case "Monday":
        console.log("Start of the work week!");
        break;
    case "Tuesday":
        console.log("Keep going!");
        break;
    case "Wednesday":
        console.log("Midweek motivation!");
        break;
    case "Thursday":
        console.log("Almost there!");
        break;
    case "Friday":
        console.log("Weekend is near!");
        break;
    case "Saturday":
        console.log("Relax, it's the weekend!");
        break;
    case "Sunday":
        console.log("Prepare for the week ahead!");
        break;
    default:
        console.log("Invalid day");
}
